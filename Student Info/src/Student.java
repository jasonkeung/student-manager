
public class Student {
	private String myName;
	private String myId;

	public Student(String name, String id) {
		myName = name;
		myId = id;
	}

	public String getMyName() {
		return myName;
	}

	public void setMyName(String myName) {
		this.myName = myName;
	}

	public String getMyId() {
		return myId;
	}

	public void setMyId(String myId) {
		this.myId = myId;
	}

}
